CREATE TABLE to_do_list(
idx INT(10) UNSIGNED ZEROFILL AUTO_INCREMENT PRIMARY KEY,
to_do_id VARCHAR(50),
owner_id VARCHAR(100),
is_checked TINYINT(1) UNSIGNED ZEROFILL,
is_canceled TINYINT(1) UNSIGNED ZEROFILL,
is_modified TINYINT(1) UNSIGNED ZEROFILL,
is_new TINYINT(1) UNSIGNED ZEROFILL,
title VARCHAR(100),
category VARCHAR(100),
detail TEXT,
comment TEXT,
create_date TIMESTAMP,
created_by VARCHAR(100),
remind_date TIMESTAMP,
start_date TIMESTAMP,
end_date TIMESTAMP,
checked_date TIMESTAMP,
priority TINYINT(1) UNSIGNED ZEROFILL
)